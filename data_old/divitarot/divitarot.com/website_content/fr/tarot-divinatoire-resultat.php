<div>On te fera prendre une décision précipitée. Attention à ne pas te laisser manipuler par une personne (h/f) plus âgée que toi qui réagit sous l’effet de la panique. Tu auras une décision majeure à prendre et il te faudra faire preuve d’une grande sagesse. Tu n’agiras pas par choix, mais bien par nécessité. L’idée de prendre une telle décision ne te plaira pas vraiment, mais on te forcera à la prendre. Il est question ici d’une personne de ta parenté directe (père, mère, frère, sœur, grand-père, grand-mère, oncle ou tante). Cette personne devra se soumettre à des soins médicaux (chirurgie ou hospitalisation). Tu auras à discuter avec des membres de ta famille à ce sujet et il vous faudra parvenir à un consensus sur ce qui doit être fait à court et à moyen terme afin que tout se déroule pour le mieux. Cette discussion n’aura pas pour principal objet l’état de santé de cette personne, mais plutôt ses conséquences sur la vie de tous et chacun (incluant la personne concernée). De plus, cet événement entraînera un déboursé d’argent qui sera assez important.</div>
<p />
<div id="numero">4.2</div>
<div>Tu auras un entretien désappointant avec une femme qui est ton amie. Plus précisément, cette femme est membre de ta parenté. Elle se distingue par sa patience et sa perspicacité. Elle est également opportuniste et quelquefois assez aventureuse. Tu vis une amitié très sincère avec elle. Suite à une conversation que tu auras avec elle à propos d’une personne (2) qui est nouvelle dans ton milieu de travail, mais avec qui tu t’apprêtes à démarrer une relation de proximité, votre entretien tournera autour de ta vie sentimentale. En fait, ton amie connaît assez bien cette personne et elle t’en dira long sur lui/elle. Tu apprendras alors que c’est une personne (2) sur laquelle tu ne peux pas compter et dont tu pourras difficilement entretenir une amitié fidèle, et encore moins une relation amoureuse. Néanmoins, cette personne (2) fera bientôt partie de ton entourage régulier et tu devras apprendre à t’épanouir avec (ou malgré) lui/elle. Attention à ne pas te fier à cette personne (promesses ou autres), car tu pourrais avoir de grandes déceptions. Même si cette personne (2) ne réussira pas à t’influencer ou à te dominer, tu comprendras rapidement à quel point ton amie avait raison à son sujet. Il est question ici d’une personne qui sera un fardeau pour toi, et ce, surtout dans les obligations qui nécessiteront un travail d'équipe. C’est une personne qui cherche continuellement à s’esquiver des tâches laborieuses ou désagréables, par conséquent il te sera totalement inutile de compter sur lui/elle lorsque tu auras besoin d’un peu plus d'entraide de sa part.</div>
<p />
<div id="numero">4.3</div>
<div>Une réponse qui t’est nécessaire (ou impérative) proviendra d'un document écrit (livre, manuscrit, lettre, attestation, etc.). Ici, il est question d’un ouvrage écrit ou d’un document rédigé à la main qui doit t’aider ou te permettre de prendre une importante décision. Cet écrit est lié à une longue démarche que tu as entreprise pour obtenir une explication, une justification, une confirmation, une autorisation ou une nouvelle connaissance. Cela concerne également une autre personne (ou ta relation avec une autre personne). Tu obtiendras ta réponse et tu deviendras quelqu’un de nouveau (spirituellement) (peut-être aussi socialement). Suite à cette réponse, tu seras en mesure d’entreprendre un nouveau cheminement dans ta vie. Ce nouveau cheminement est très significatif pour toi, car au fil du temps il t’apportera encore d’autres réponses qui seront encore plus importantes pour comprendre ton destin (ce que te réserve ton avenir à long terme). Tu as un destin qui est vraiment unique et personne d’autre ne vivra ce que tu vivras.</div>
